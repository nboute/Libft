#include <string.h>

int		ft_memcmp(const void *ptr1, const void *ptr2, size_t n)
{
	char	*p1;
	char	*p2;

	p1 = (char*)ptr1;
	p2 = (char*)ptr2;
	while (n > 0 && *p1 == *p2)
	{
		n--;
		p1++;
		p2++;
	}
	return ((int)(*p1 - *p2));
}
